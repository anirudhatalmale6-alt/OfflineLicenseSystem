using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Security.Cryptography;
using System.Text;
using System.Text.Json;

namespace LicenseVerifier
{
    /// <summary>
    /// License data model containing all fields from the license file.
    /// </summary>
    public class LicenseInfo
    {
        public string LicenseId { get; set; } = "";
        public string LicenseeName { get; set; } = "";
        public string LicenseeEmail { get; set; } = "";
        public string CompanyName { get; set; } = "";
        public string ProductName { get; set; } = "";
        public string ProductVersion { get; set; } = "";
        public string LicenseType { get; set; } = "";
        public DateTime IssuedDate { get; set; }
        public DateTime ExpiryDate { get; set; }
        public int MaxMachines { get; set; }
        public string Features { get; set; } = "";

        public bool IsExpired => DateTime.Now.Date > ExpiryDate;

        public List<string> FeatureList =>
            string.IsNullOrEmpty(Features)
                ? new List<string>()
                : Features.Split(',').Select(f => f.Trim()).ToList();

        public override string ToString()
        {
            return $"License ID:    {LicenseId}\n" +
                   $"Licensee:      {LicenseeName} ({LicenseeEmail})\n" +
                   $"Company:       {CompanyName}\n" +
                   $"Product:       {ProductName} v{ProductVersion}\n" +
                   $"Type:          {LicenseType}\n" +
                   $"Issued:        {IssuedDate:yyyy-MM-dd}\n" +
                   $"Expires:       {ExpiryDate:yyyy-MM-dd}\n" +
                   $"Max Machines:  {MaxMachines}\n" +
                   $"Features:      {Features}\n" +
                   $"Status:        {(IsExpired ? "EXPIRED" : "ACTIVE")}";
        }
    }

    /// <summary>
    /// Result of a license verification attempt.
    /// </summary>
    public class LicenseValidationResult
    {
        public bool IsValid { get; set; }
        public string ErrorMessage { get; set; } = "";
        public LicenseInfo? License { get; set; }
    }

