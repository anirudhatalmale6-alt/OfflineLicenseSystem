        /// <summary>
        /// Validate a license file from a file path.
        /// </summary>
        public LicenseValidationResult ValidateLicenseFile(string filePath)
        {
            try
            {
                if (!File.Exists(filePath))
                {
                    return new LicenseValidationResult
                    {
                        IsValid = false,
                        ErrorMessage = $"License file not found: {filePath}"
                    };
                }

                string content = File.ReadAllText(filePath);
                return ValidateLicenseContent(content);
            }
            catch (Exception ex)
            {
                return new LicenseValidationResult
                {
                    IsValid = false,
                    ErrorMessage = $"Error reading license file: {ex.Message}"
                };
            }
        }

        /// <summary>
        /// Validate license from raw content string.
        /// </summary>
        public LicenseValidationResult ValidateLicenseContent(string licenseContent)
        {
            try
            {
                // Parse the license file
                var lines = licenseContent.Trim().Split('\n')
                    .Select(l => l.Trim())
                    .Where(l => !string.IsNullOrEmpty(l))
                    .ToArray();

                if (lines.Length < 4 ||
                    lines[0] != "----BEGIN LICENSE----" ||
                    lines[2] != "----BEGIN SIGNATURE----" ||
                    lines[4] != "----END LICENSE----")
                {
                    return new LicenseValidationResult
                    {
                        IsValid = false,
                        ErrorMessage = "Invalid license file format."
                    };
                }

                string dataBase64 = lines[1];
                string signatureBase64 = lines[3];

                // Decode the data and signature
                byte[] dataBytes = Encoding.UTF8.GetBytes(
                    Encoding.UTF8.GetString(Convert.FromBase64String(dataBase64))
                );
                byte[] signatureBytes = Convert.FromBase64String(signatureBase64);

                // Verify the signature using RSA + SHA256
                bool signatureValid = VerifySignature(dataBytes, signatureBytes);

                if (!signatureValid)
                {
                    return new LicenseValidationResult
                    {
                        IsValid = false,
                        ErrorMessage = "License signature verification failed. The license may be tampered with."
                    };
                }

                // Parse the license JSON data
                string jsonString = Encoding.UTF8.GetString(Convert.FromBase64String(dataBase64));
                var licenseInfo = ParseLicenseJson(jsonString);

                return new LicenseValidationResult
                {
                    IsValid = true,
                    License = licenseInfo
                };
            }
            catch (Exception ex)
            {
                return new LicenseValidationResult
                {
                    IsValid = false,
                    ErrorMessage = $"License validation error: {ex.Message}"
                };
            }
        }

        /// <summary>
        /// Verify RSA-SHA256 signature using the embedded public key.
        /// </summary>
        private bool VerifySignature(byte[] data, byte[] signature)
        {
            using var rsa = RSA.Create();
            rsa.ImportFromPem(PublicKeyPem.ToCharArray());

            return rsa.VerifyData(
                data,
                signature,
                HashAlgorithmName.SHA256,
                RSASignaturePadding.Pkcs1
            );
        }

        /// <summary>
        /// Parse the JSON license data into a LicenseInfo object.
        /// </summary>
        private LicenseInfo ParseLicenseJson(string json)
        {
            using var doc = JsonDocument.Parse(json);
            var root = doc.RootElement;

            return new LicenseInfo
            {
                LicenseId = GetStringOrDefault(root, "license_id"),
                LicenseeName = GetStringOrDefault(root, "licensee_name"),
                LicenseeEmail = GetStringOrDefault(root, "licensee_email"),
                CompanyName = GetStringOrDefault(root, "company_name"),
                ProductName = GetStringOrDefault(root, "product_name"),
                ProductVersion = GetStringOrDefault(root, "product_version"),
                LicenseType = GetStringOrDefault(root, "license_type"),
                IssuedDate = DateTime.Parse(GetStringOrDefault(root, "issued_date", "2000-01-01")),
                ExpiryDate = DateTime.Parse(GetStringOrDefault(root, "expiry_date", "2000-01-01")),
                MaxMachines = GetIntOrDefault(root, "max_machines", 1),
                Features = GetStringOrDefault(root, "features"),
            };
        }

        private static string GetStringOrDefault(JsonElement root, string property, string defaultValue = "")
        {
            return root.TryGetProperty(property, out var el) ? el.GetString() ?? defaultValue : defaultValue;
        }

        private static int GetIntOrDefault(JsonElement root, string property, int defaultValue = 0)
        {
            if (!root.TryGetProperty(property, out var el)) return defaultValue;
            return el.ValueKind == JsonValueKind.Number ? el.GetInt32() : defaultValue;
        }
    }
