    /// Verifies digitally signed license files generated by the PHP server.
    ///
    /// Usage:
    ///   var validator = new LicenseValidator();
    ///   var result = validator.ValidateLicenseFile("path/to/license.lic");
    ///   if (result.IsValid && !result.License.IsExpired) { /* licensed */ }
    /// </summary>
    public class LicenseValidator
    {
